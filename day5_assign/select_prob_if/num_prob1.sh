read -p "enter the single digit num : " num

if [ $num -eq 0 ]
then
	echo "$num : zero"
elif [ $num -eq 1 ]
then
	echo "$num : one"
elif [ $num -eq 2 ]
then
        echo "$num : two"
elif [ $num -eq 3 ]
then
        echo "$num : three"
elif [ $num -eq 4 ]
then
        echo "$num : four"
elif [ $num -eq 5 ]
then
        echo "$num : five"
elif [ $num -eq 6 ]
then
        echo "$num : six"
elif [ $num -eq 7 ]
then
        echo "$num : seven"
elif [ $num -eq 8 ]
then
        echo "$num : eight "
elif [ $num -eq 9 ]
then
        echo "$num : nine"

else
	echo "please enter the valid number "
fi	

	

